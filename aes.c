static int RotByte(int i) {
    if (i%4 == 3) {
        return 7;
    } else { 
        return 3;
    }
}

/* This is here to make the operations of x1, x2, x3 parallel */
static unsigned char times1(unsigned char n) {
    return n;
}
static unsigned char times2(unsigned char n) {
    /* Return 2*n, computed in GF(2^8) with the Rijndael representation */
    return (n << 1) ^ (n & 0x80 ? 0x01b : 0x00);
}
static unsigned char times3(unsigned char n) {
    /* Return 3*n, computed in GF(2^8) with the Rijndael representation */
    return n ^ times2(n);  /* 3n = n + 2n */
}

int AES_set_encrypt_key(const unsigned char *userKey, int bits,
                        AES_KEY *key)
{
    int i;
    unsigned char *W = key->round_key;
    unsigned char rcon = 0x01;

    if (bits != 128) return -1;   /* We support only 128 bits key size */

    for (i = 0; i < 16; i++) {
        W[i] = userKey[i];
    }

    for (i = 16; i < 16 * (10 + 1); i++) {
        unsigned char temp;
        if ((i % 16) < 4) {
            temp = sbox[W[i - RotByte(i)]];
            if ((i % 16) == 0) {
                temp ^= rcon;
                rcon = times2(rcon);
            }
        } else {
            temp = W[i - 4];
        }
        W[i] = W[i - 16] ^ temp;
    }

    return 0;
}

static void ByteSub(unsigned char *block) {
    int i;
    for (i=0; i<AES_BLOCK_SIZE; i++) {
        block[i] = sbox[ block[i] ];
    }
}

static void ShiftRow(unsigned char *block) {
    int i, j;
    unsigned char row[4];
    int shift = 0;
    for (i=0; i<4; i++, shift = (shift+3)%4) {
        for (j=0; j<4; j++) {
            row[ (j+shift)%4 ] = block[ i+4*j ];
        }
        for (j=0; j<4; j++) {
            block[i+4*j] = row[ j ];
        }
    }
}

static void MixColumn(unsigned char *block) {
    int i, j;
    for (i=0; i<4; i++) {
        unsigned char column[4];
        unsigned char a0 = block[ 4*i + 0 ];
        unsigned char a1 = block[ 4*i + 1 ];
        unsigned char a2 = block[ 4*i + 2 ];
        unsigned char a3 = block[ 4*i + 3 ];

        column[0] = times2(a0) ^ times3(a1) ^ times1(a2) ^ times1(a3);
        column[1] = times1(a0) ^ times2(a1) ^ times3(a2) ^ times1(a3);
        column[2] = times1(a0) ^ times1(a1) ^ times2(a2) ^ times3(a3);
        column[3] = times3(a0) ^ times1(a1) ^ times1(a2) ^ times2(a3);

        for (j=0; j<4; j++) {
            block[4*i+j] = column[ j ];
        }
    }
}

static void AddRoundKey(unsigned char *block, const unsigned char *round_key) {
    int i;

    for (i=0; i<AES_BLOCK_SIZE; i++) {
        block[i] ^= round_key[i];
    }
}

void AES_ecb_encrypt(const unsigned char *in, unsigned char *out,
        const AES_KEY *key, int enc) {
    unsigned char block[AES_BLOCK_SIZE];
    int i;
    int round;

    for (i=0; i<AES_BLOCK_SIZE; i++) block[i] = in[i];

    AddRoundKey( block, &key->round_key[ 0 ] );

    for (round=1; round<10; round++) {
        ByteSub( block );
        ShiftRow( block );
        MixColumn( block );
        AddRoundKey( block, &key->round_key[ 16 * round ] );
    }

    /* Last round is slightly different */
    ByteSub( block );
    ShiftRow( block );
    AddRoundKey( block, &key->round_key[ 16 * round ] );

    for (i=0; i<AES_BLOCK_SIZE; i++) out[i] = block[i];
}
